    private void btnBusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBusActionPerformed
       
        PreparedStatement ps = null;
        try {
            Conexion objCon = new Conexion();
            Connection conn = objCon.getConexion();
            ps = conn.prepareStatement("INSERT INTO usuarios(nombre,email,telefono) VALUES (?,?,?)");
            ps.setString(1, txtBus.getText());
            ps.setString(2, txtEmail.getText());
            ps.setString(3, txtTelefono.getText());

            ps.execute();

            JOptionPane.showMessageDialog(null, "Uusuario Guardado");
            Object[] fila = new Object[4];
            fila[0] = txtBus.getText();
            fila[1] = txtEmail.getText();
            fila[2] = txtTelefono.getText();
            modelo.addRow(fila);

            limpiar();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al Guardar Uusuario");
            System.out.println(ex);
        }
        
    }//GEN-LAST:event_btnBusActionPerformed

