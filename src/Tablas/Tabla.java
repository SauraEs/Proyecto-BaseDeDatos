    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int Fila = jDatos.getSelectedRow();

        PreparedStatement ps = null;
        
        try {
            Conexion objCon = new Conexion();
            Connection conn = objCon.getConexion();
            ps = conn.prepareStatement("UPDATE usuarios SET email=?,telefono=? WHERE nombre=?");

            ps.setString(1, txtEmail.getText());
            ps.setString(2, txtTelefono.getText());
            ps.setString(3, txtBus.getText());

            ps.execute();

            JOptionPane.showMessageDialog(null, "Uusuario Modificado");
            jDatos.setValueAt(txtBus.getText(), Fila, 0);
            jDatos.setValueAt(txtEmail.getText(), Fila, 1);
            jDatos.setValueAt(txtTelefono.getText(), Fila, 2);

            limpiar();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al Modificar Uusuario");
            System.out.println(ex);
        }

        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jDatosMouseClicked
        
        PreparedStatement ps = null;
        ResultSet rs = null;
        
        try {
            
            Conexion objCon = new Conexion();
            Connection conn = objCon.getConexion();
            
            int fila = jDatos.getSelectedRow();
            String nombre = jDatos.getValueAt(fila,0).toString();
            
            ps = conn.prepareStatement("SELECT nombre,email,telefono FROM usuarios WHERE nombre =?");
            ps.setString(1, nombre);
            rs = ps.executeQuery();
            
            while (rs.next()) {
                
                txtBus.setText(rs.getString("nombre"));
                txtEmail.setText(rs.getString("email"));
                txtTelefono.setText(rs.getString("telefono"));
            }
            
        } catch (Exception ex) {
            
            System.out.println(ex.toString());
            
        }
        
    }//GEN-LAST:event_jDatosMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        PreparedStatement ps = null;
        try {

            Conexion objCon = new Conexion();
            Connection conn = objCon.getConexion();

            int Fila = jDatos.getSelectedRow();
            String nombre = jDatos.getValueAt(Fila, 0).toString();

            ps = conn.prepareStatement("DELETE FROM usuarios WHERE nombre=?");
            ps.setString(1, nombre);
            ps.execute();

            modelo.removeRow(Fila);
            JOptionPane.showMessageDialog(null, "Uusuario Eliminado");
            limpiar();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al Eliminar Uusuario");
            System.out.println(ex.toString());
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

